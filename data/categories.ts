export function getCategoryBySlug(slug: string): Category | undefined {
  return CATEGORIES.find((c) => c.slug === slug);
}

export function getSubcategoryBySlug(catSlug: string, subSlug: string) {
  const cat = getCategoryBySlug(catSlug);
  return cat?.subcategories.find((s) => s.slug === subSlug);
}

export function getCategoryKeywords(category: Category): string[] {
  return [
    category.name,
    category.slug.replace(/-/g, " "),
    ...category.keywords,
    ...category.subcategories.flatMap((s) => [
      s.name,
      s.slug.replace(/-/g, " "),
      ...s.keywords,
    ]),
  ];
}